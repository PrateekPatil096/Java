public class loops {
    public static void main(String args[]){
        for(int i=0;i<11;i=i+1){
            System.out.println("toji");
        }
        for(int j=0;j<11;j++){
            System.out.println(j+" ");

        }
        int k=0;
        while(k<11){
            System.out.println(k);
            k++;
         }

         int h=0;
         do{
            System.out.println(h);
            h++;
         }while(h<11);

    }
    
}
